-- SETUPS TABLE --
-- Constraints added manually to ensure that all values are within in-game parameters
CREATE TABLE Setups (
    id INTEGER PRIMARY KEY AUTOINCREMENT,

    -- Aerodynamics --
    front_wing_aero INTEGER NOT NULL CHECK (front_wing_aero BETWEEN 0 AND 50),
    rear_wing_aero INTEGER NOT NULL CHECK (rear_wing_aero BETWEEN 0 AND 50),

    -- Transmission --
    -- Throttle Differentials - steps of 5
    on_throttle_differential INTEGER NOT NULL CHECK (
        on_throttle_differential BETWEEN 10 AND 100
        AND (on_throttle_differential - 10) % 5 = 0
    ),
    off_throttle_differential INTEGER NOT NULL CHECK (
        off_throttle_differential BETWEEN 10 AND 100
        AND (off_throttle_differential - 10) % 5 = 0
    ),
    -- Engine Braking - steps of 10
    engine_braking INTEGER NOT NULL CHECK (
        engine_braking BETWEEN 0 AND 100
        AND engine_braking % 10 = 0
    ),

    -- Suspension Geometry --

    -- Camber - steps of 0.10
    -- Used scaled integers for decimal setup values to ensure precision and enforce valid increments
    -- converted using /utils/recordConverter.js function during validation process 
    front_camber INTEGER NOT NULL CHECK (
        front_camber BETWEEN -350 AND -250
        AND (front_camber + 350) % 10 = 0
    ),
    rear_camber INTEGER NOT NULL CHECK (
        rear_camber BETWEEN -220 AND -70
        AND (rear_camber + 220) % 10 = 0
    ),

    -- Toe - steps of 0.01
    -- Used scaled integers for decimal setup values to ensure precision and enforce valid increments
    -- converted using /utils/recordConverter.js function during validation process 
    front_toe INTEGER  NOT NULL CHECK (
        front_toe BETWEEN 0 AND 50
    ),
    rear_toe INTEGER NOT NULL CHECK (
        rear_toe BETWEEN 0 AND 50
    ),

    -- Suspension --
    front_suspension INTEGER NOT NULL CHECK (front_suspension BETWEEN 1 AND 41),
    rear_suspension INTEGER NOT NULL CHECK (rear_suspension BETWEEN 1 AND 41),
    front_anti_roll_bar INTEGER NOT NULL CHECK (front_anti_roll_bar BETWEEN 1 AND 21),
    rear_anti_roll_bar INTEGER NOT NULL CHECK (rear_anti_roll_bar BETWEEN 1 AND 21),
    front_ride_height INTEGER NOT NULL CHECK (front_ride_height BETWEEN 10 AND 40),
    rear_ride_height INTEGER NOT NULL CHECK (rear_ride_height BETWEEN 40 AND 100),

    -- Brakes --
    brake_pressure INTEGER NOT NULL CHECK (brake_pressure BETWEEN 80 AND 100),
    front_brake_bias INTEGER NOT NULL CHECK (front_brake_bias BETWEEN 50 AND 70),

    -- Tyres --
    -- Front and rear tyre pressures - steps of 0.1
    -- Used scaled integers for decimal setup values to ensure precision and enforce valid increments
    -- const dbValue = Math.round(userInput * 10); // -> 225
    -- const displayValue = dbValue / 10; // -> 22.5 psi
    front_left_tyre_pressure INTEGER NOT NULL CHECK (
        front_left_tyre_pressure BETWEEN 225 AND 295
    ),
    front_right_tyre_pressure INTEGER NOT NULL CHECK (
        front_right_tyre_pressure BETWEEN 225 AND 295
    ),
    rear_left_tyre_pressure INTEGER NOT NULL CHECK (
        rear_left_tyre_pressure BETWEEN 205 AND 265
    ),
    rear_right_tyre_pressure INTEGER NOT NULL CHECK (
        rear_right_tyre_pressure BETWEEN 205 AND 265
    )
);

-- LAP RECORDS TABLE --
CREATE TABLE LapRecords (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    circuit_id INTEGER NOT NULL,
    constructor_id INTEGER NOT NULL,
    setup_id INTEGER NOT NULL,
    --For laptime storage simplification, there are store in milliseconds. 
    --The display logic will convert them back to the standard mm:ss.mmm format with parse function.
    lap_time_ms INTEGER NOT NULL CHECK (lap_time_ms > 0),
    weather TEXT NOT NULL CHECK (weather IN ('Dry', 'Wet')),
    controller_type TEXT NOT NULL CHECK (controller_type IN ('Controller', 'Wheel')),
    record_date TEXT NOT NULL CHECK (date(record_date) IS NOT NULL),

    FOREIGN KEY (circuit_id) REFERENCES Circuits(id),
    FOREIGN KEY (constructor_id) REFERENCES Constructors(id),
    FOREIGN KEY (setup_id) REFERENCES Setups(id)
);

